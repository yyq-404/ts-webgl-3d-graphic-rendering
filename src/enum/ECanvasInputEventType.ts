/**
 * 输入事件类型。
 */
export enum ECanvasInputEventType {
    /** 鼠标事件 */
    MOUSE_EVENT,
    /** 鼠标抬起 */
    MOUSE_UP,
    /** 鼠标按下 */
    MOUSE_DOWN,
    /** 鼠标移动 */
    MOUSE_MOVE,
    /** 鼠标拖动 */
    MOUSE_DRAG,
    /** 键盘事件 */
    KEYBOARD_EVENT,
    /** 按键抬起 */
    KEY_UP,
    /** 按键按下 */
    KEY_DOWN,
    /** 按键事件 */
    KEY_PRESS
}